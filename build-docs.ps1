 Produces the three API-help outputs from ONE repeatable, idempotent script:
   1. DocFX static site  -> docs-site\            (ready for GitHub Pages) + link-check
   2. CHM                -> Help\BastionKryptonSuite.chm      (Sandcastle Help File Builder)
   3. PDF developer guide-> Help\BastionKryptonSuite-Manual.pdf (pandoc + wkhtmltopdf)
 Plus an XML-doc coverage report -> docs\audit\XMLDOC-COVERAGE.md.

 USAGE (run from the repository root):
   .\build-docs.ps1 -All        # coverage + site + chm + pdf
   .\build-docs.ps1 -Site       # DocFX site + link-check only
   .\build-docs.ps1 -Chm        # CHM only
   .\build-docs.ps1 -Pdf        # PDF only
   .\build-docs.ps1 -Coverage   # XML-doc coverage report only
 (any combination of switches may be given; -All implies all four)

 PREREQUISITE: the suite's net8.0-windows assemblies + XML doc files must already be
 built (Phase 2 output). This script consumes them; it does not build the libraries.
   Core:     Standard-Toolkit\Bin\Release\net8.0-windows\*.dll + *.xml
   Extended: Extended-Toolkit\Bin\Release\Krypton.Toolkit.Suite.Extended.Ultimate\net8.0-windows\

 ----------------------------------------------------------------------------------
 CI notes (Phase 8):
   * The DocFX site + the XML-doc coverage report are fully CI-friendly on any
     windows-latest (or even linux) runner: they need only the .NET SDK, the docfx
     dotnet tool, and the prebuilt net8 assemblies+XML. GitHub Actions CAN build and
     publish docs-site\ to GitHub Pages.
   * The CHM needs WINDOWS-ONLY tooling: Sandcastle Help File Builder (SHFB) + HTML Help
     Workshop (hhc.exe) + desktop MSBuild. windows-latest runners do not have these by
     default - install SHFB/HHW in the job or run this step on a self-hosted Windows
     agent. It CANNOT be produced on Linux/macOS.
   * The PDF needs wkhtmltopdf + pandoc installed on the runner (both are available for
     windows-latest via choco/winget; wkhtmltopdf is Windows/Linux, so a Linux runner
     with pandoc+wkhtmltopdf can also produce it).
   * Generated outputs (docs-site\, Help\*.chm, Help\*.pdf, artifacts\) are NOT committed
     - they are build artefacts regenerated by CI/release.
